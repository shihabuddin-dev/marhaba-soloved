/* 
৫. একটা truthy ভেরিয়েবল grapes = 19 দিয়ে ||= অপারেটর ব্যবহার করলে এর মান কী হবে এবং কেন?
 */
let grapes = 19;
grapes ||= 50;
console.log(grapes);


/* 
||= অপারেটর চেক করে যদি ভেরিয়েবল falsy হয়, তাহলে নতুন মান সেট করে।

কিন্তু 19 একটি truthy value, তাই নতুন মান 50 সেট হবে না।

তাই grapes-এর মান আগের মতোই 19 থাকবে।
*/