/* 
৯. apples = 15 হলে, apples % = 6 ব্যবহার করলে আউটপুট কত হবে এবং সেটা কেন তত হবে 
*/
let apples = 15
apples %= 6
console.log(apples);

/* কারণ

%= হল modulus assignment অপারেটর, যা apples এর বর্তমান মানকে 6 দিয়ে ভাগ করে অবশিষ্টাংশ (remainder) বের করে এবং সেটিকে আবার apples এ অ্যাসাইন করে।

অর্থাৎ, 15 % 6 হলো 3, কারণ ১৫ কে ৬ দিয়ে ভাগ করলে ভাগফল ২ এবং অবশিষ্টাংশ (remainder) হয়  তাই, apples এর নতুন মান হবে 3। 
*/